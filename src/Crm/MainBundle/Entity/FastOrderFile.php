<?php

namespace Crm\MainBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * FastOrderFile
 *
 * @ORM\Table()
 * @ORM\Entity
 */
class FastOrderFile
{
    /**
     * @var integer
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="title", type="string", length=255)
     */
    private $title;

    /**
     * @var array
     *
     * @ORM\Column(name="file", type="array", nullable=true)
     */
    private $file;

    /**
     * @ORM\ManyToOne(targetEntity="Crm\MainBundle\Entity\FastOrder", inversedBy="files")
     */
    private $order;

    public function __construct()
    {
        $this->file = [];
        $this->title = 'Другой документ';
    }

    public function __toString()
    {
        return $this->getTitle();
    }

    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set title
     *
     * @param string $title
     *
     * @return FastOrderFile
     */
    public function setTitle($title)
    {
        $this->title = $title;

        return $this;
    }

    /**
     * Get title
     *
     * @return string
     */
    public function getTitle()
    {
        return $this->title;
    }

    /**
     * Set file
     *
     * @param array $file
     *
     * @return FastOrderFile
     */
    public function setFile($file)
    {
        $this->file = $file;

        return $this;
    }

    /**
     * Get file
     *
     * @return array
     */
    public function getFile()
    {
        return $this->file;
    }

    /**
     * @return mixed
     */
    public function getOrder()
    {
        return $this->order;
    }

    /**
     * @param mixed $order
     */
    public function setOrder($order)
    {
        $this->order = $order;
    }


}

